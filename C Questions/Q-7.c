// Write a program to print formatted output.


#include <stdio.h>
#include <stdlib.h>
int main()
{
    printf("Hello\bworld!\n");//Backspace
    printf("Hello\tworld!\n");//tab space
    printf("Hello\nworld!\n");//New line
    printf("Hello\rworld!");//Carriage return
    return 0;
}

